import type { NextFunction, Request, Response } from 'express';
import { collectDefaultMetrics, Counter, Histogram, Registry } from 'prom-client';

import { env } from '../config/env.js';

const registry = new Registry();

collectDefaultMetrics({
  register: registry,
  prefix: 'movemarket_',
});

const httpRequestCounter = new Counter({
  name: 'movemarket_http_requests_total',
  help: 'Total number of HTTP requests received',
  labelNames: ['method', 'route', 'status'],
  registers: [registry],
});

const httpRequestDuration = new Histogram({
  name: 'movemarket_http_request_duration_seconds',
  help: 'Histogram of HTTP request durations in seconds',
  labelNames: ['method', 'route', 'status'],
  buckets: [0.005, 0.01, 0.025, 0.05, 0.1, 0.25, 0.5, 1, 2, 5],
  registers: [registry],
});

const settlementExecutionCounter = new Counter({
  name: 'movemarket_settlement_executions_total',
  help: 'Total settlement queue execution attempts',
  labelNames: ['chain', 'result'],
  registers: [registry],
});

const settlementBatchHistogram = new Histogram({
  name: 'movemarket_settlement_batch_request',
  help: 'Requested number of tickets to process per settlement execution',
  labelNames: ['chain'],
  buckets: [1, 5, 10, 25, 50, 100],
  registers: [registry],
});

const claimTicketBuildCounter = new Counter({
  name: 'movemarket_claim_ticket_build_total',
  help: 'Number of claim ticket redemption transactions generated by the backend',
  labelNames: ['chain'],
  registers: [registry],
});

const marketResolutionCounter = new Counter({
  name: 'movemarket_market_resolution_total',
  help: 'Market resolution attempts initiated by the backend',
  labelNames: ['chain', 'result'],
  registers: [registry],
});

const suiIndexerPollCounter = new Counter({
  name: 'movemarket_sui_indexer_polls_total',
  help: 'Sui event indexer poll attempts',
  labelNames: ['result'],
  registers: [registry],
});

const suiEventsProcessedCounter = new Counter({
  name: 'movemarket_sui_events_processed_total',
  help: 'Count of Sui events processed by the backend indexer',
  labelNames: ['event'],
  registers: [registry],
});

const normalizePath = (path: string): string => {
  if (!path) {
    return 'unknown';
  }

  const cleaned = path.split('?')[0] ?? path;
  // Collapse numeric ids and long hex strings to avoid high-cardinality metrics.
  return cleaned.replace(/([0-9a-fA-F]{8,}|\d+)/g, ':param');
};

export const metricsMiddleware = (req: Request, res: Response, next: NextFunction) => {
  const endTimer = httpRequestDuration.startTimer();

  res.on('finish', () => {
    const route = normalizePath(req.route?.path ?? req.originalUrl ?? req.path);
    const labels = {
      method: req.method,
      route,
      status: String(res.statusCode),
    };

    httpRequestCounter.inc(labels);
    endTimer(labels);
  });

  next();
};

export const metricsHandler = async (req: Request, res: Response) => {
  if (env.METRICS_AUTH_TOKEN) {
    const provided = req.header('x-metrics-token') ?? req.header('authorization');
    if (!provided || provided.replace(/^Bearer\s+/i, '') !== env.METRICS_AUTH_TOKEN) {
      return res.status(401).json({ error: 'Unauthorized' });
    }
  }

  res.setHeader('Content-Type', registry.contentType);
  res.send(await registry.metrics());
};

export const recordSettlementExecution = (
  chain: string,
  batchSize: number,
  result: 'success' | 'failure'
) => {
  settlementExecutionCounter.inc({ chain, result });
  settlementBatchHistogram.observe({ chain }, batchSize);
};

export const recordClaimTicketBuild = (chain: string) => {
  claimTicketBuildCounter.inc({ chain });
};

export const recordMarketResolution = (chain: string, result: 'success' | 'failure') => {
  marketResolutionCounter.inc({ chain, result });
};

export const recordSuiIndexerPoll = (result: 'success' | 'failure') => {
  suiIndexerPollCounter.inc({ result });
};

export const recordSuiEventsProcessed = (event: string, count: number) => {
  suiEventsProcessedCounter.inc({ event }, count);
};
